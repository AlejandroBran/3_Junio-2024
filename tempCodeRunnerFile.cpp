int suma(int entero1, int entero2){
int resultado=0;
resultado = (entero1 + entero2);
return resultado;
}

int resta(int entero1, int entero2){
int resultado=0;
resultado = (entero1 - entero2);
return resultado;
}

int multiplicacion(int entero1, int entero2){
int resultado=0;
resultado = (entero1 * entero2);
return resultado;
}

float division(float entero1, float entero2){
float resultado=0;
resultado = (entero1 / entero2);
return resultado;
}